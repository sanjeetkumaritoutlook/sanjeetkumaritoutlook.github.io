import { Component, h } from '@stencil/core';
//by default it shows in boilerplate code, createRouter also imported in same file
import { Router } from "../../";  //in index.ts its already there so no need to again do const Router = createRouter();

import { Route, match } from "stencil-router-v2";
//https://www.npmjs.com/package/stencil-router-v2
@Component({
  tag: 'app-root-portfolio',
  styleUrl: 'app-root-portfolio.css',
  shadow: true,
})
export class AppRoot {
  render() {
    return (
      <div>
        <header>
            <h1 onClick={() => Router.push("/")}>
              Home
            </h1>
            <header-navigation></header-navigation>
        </header>
        <h1>Pages</h1>
        <main>
          <Router.Switch>
            <Route path="/">
              <app-home-page />
            </Route>

            <Route 
             path={/^\/posts-dashboard/}>
            <posts-dashboard></posts-dashboard>
            </Route> 

            <Route
             path={/^\/web-components/   }>
            <web-components></web-components>
            </Route> 
            <Route
             path={/^\/feedback-form/   }>
            <feedback-form></feedback-form>
            </Route> 
            <Route
             path={/^\/about-us/   }>
            <about-us></about-us>
            </Route> 
            <Route
              path={match("/profile/:name")}
              render={({ name }) => <app-profile-page name={name} />}
            />
          </Router.Switch>
        </main>
      </div>
    );
  }
}
